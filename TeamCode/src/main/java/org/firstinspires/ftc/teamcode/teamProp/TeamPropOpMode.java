@TeleOp
public class TeamPropOpMode extends LinearOpMode {
    OpenCvCamera camera;
    TeamPropPipeline teamPropPipeline;

    static final int CAMERA_WIDTH = 800;
    static final int CAMERA_HEIGHT = 600;
    static final double CAMERA_OFFSET = 00000.0 / 12; // in feet

    static final double FEET_PER_METER = 3.28084;

    // Lens intrinsics
    // UNITS ARE PIXELS
    // NOTE: this calibration is for the C920 webcam at 800x448.
    // You will need to do your own calibration for other configurations!
    double fx = 578.272;
    double fy = 578.272;
    double cx = 402.145;
    double cy = 221.506;

    @Override
    public void runOpMode() {

        int cameraMonitorViewId = hardwareMap.appContext.getResources().getIdentifier("cameraMonitorViewId", "id", hardwareMap.appContext.getPackageName());
        WebcamName webcamName = hardwareMap.get(WebcamName.class, "Webcam");
        camera = OpenCvCameraFactory.getInstance().createWebcam(webcamName, cameraMonitorViewId);

        teamPropPipeline = new TeamPropPipeline(fx, fy, cx, cy, telemetry);

        camera.setPipeline(teamPropPipeline);
        camera.openCameraDeviceAsync(new OpenCvCamera.AsyncCameraOpenListener() {
            @Override
            public void onOpened() {
                camera.startStreaming(CAMERA_WIDTH, CAMERA_HEIGHT, OpenCvCameraRotation.UPRIGHT);
            }

            @Override
            public void onError(int errorCode) {

            }
        });

        telemetry.setMsTransmissionInterval(50);

    /*
     * The INIT-loop:
     * This REPLACES waitForStart!
     */

        while (!isStarted() && !isStopRequested())
    {
        if (Objects.equals(teamPropPipeline.getLatestPosition(), new Point(0, 0))){
            telemetry.addLine("Nothing is detected");
        }
        else {
            telemetry.addLine("X Position is:" + teamPropPipeline.getLatestPosition().x + "Y Position is:" + teamPropPipeline.getLatestPosition().y);
        }
        telemetry.update();
    }


    /* You wouldn't have this in your autonomous, this is just to prevent the sample from ending */
        while (opModeIsActive()) {sleep(20);}
}

}